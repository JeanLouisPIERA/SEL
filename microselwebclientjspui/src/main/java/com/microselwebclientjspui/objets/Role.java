package com.microselwebclientjspui.objets;

public class Role {
	
	String id;

	String name;
	
	public Role() {
		super();

	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	


}
